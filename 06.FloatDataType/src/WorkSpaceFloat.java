
public class WorkSpaceFloat {
	public static void main(String[] args) {
		
		// Ondalıklı sayılar tanımlamak için kullanılır. Virgülden sonra 7 basamağa kadar değer alabilir.
		// 4 byte'lık yer kaplar.
		// float veri tipindeki değişkenime değer atarken atadığım değerin sonuna küçük f veya
		// büyük F yazıyorumki
		// atamış olduğum değerin float olduğunu anlasın compiler 
		// float veri tipindeki bir değişkende değerin noktadan sonra ve öncesi toplam 8 basamak olabilir.
		
		
		float x = 1.1234567f ;
		float y = 1234567.1f;
		float z = 1.5858585F;
		
		System.out.println(z);
		System.out.println(x);
		System.out.println(y);

	}
}
