
public class WorkSpace_Arttırma_Azaltma_Operatörler {
		public static void main(String[] args) {
		
		// arttırma : ++ Bir değişkenin değerini 1 arttırmak için kullanılır
		// azaltma : --  Bir değişkenin değerini 1 azaltmak için kullanılır.
		
			int x = 6; 
			x++; // 1 arttır değerini 
			x++; // bir kez daha 1 arttır
			System.out.println(x);
			
			int y = 5;
			y--; // 1 azalt değerini
			System.out.println(y);
			
			
			
		}

}
