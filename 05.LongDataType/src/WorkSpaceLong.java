
public class WorkSpaceLong {
	public static void main(String[] args) {
		
		// Long veri tipi çok büyük tam sayı değerler için kullanılır ( Kentilyona kadar değer alabilir)
		// 8 byte'lık alan kaplar 
		
	long maxDeger = Long.MAX_VALUE; 
	
	long minDeger = Long.MIN_VALUE;
	
	// Anlamı şu long veri tipinin maksimum değerini al ve onu getir değişkenimin içine koy
	
	System.out.println(maxDeger);
	System.out.println(minDeger);
	
		


	long x = 123456789123L;
	System.out.println(x);

	
	// Long veri tipinde bir değişken belirlerken değişkenime atadığım değerin sonuna büyük L koyuyorum.
	
	
		
		
	}
}
