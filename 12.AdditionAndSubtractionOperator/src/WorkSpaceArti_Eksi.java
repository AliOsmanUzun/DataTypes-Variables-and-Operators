
public class WorkSpaceArti_Eksi {
	public static void main(String[] args) {
		
		// + and - operators

		// + operatörü

		int number1 = 58;
		int number2 = 14;
		int number3 = 17;

		int result = (number1 + number2 + number3) / 3;

		// artı operatörü değişkenleri ekrana yazdırırken birleştirme amaçlıda
		// kullanılıyor
		System.out.println("Ders geçme notunuz : " + result);

		// - operatörü

		int cikart1 = 158;
		int cikart2 = 64;

		int cikartsonuc = cikart1 - cikart2;

		System.out.println(cikartsonuc);

		// + operatörü değişkenleri birleştirir

		String metinAd = "Ali Osman";
		String metinSoyisim = "Uzun";

		System.out.println(metinAd + " " + metinSoyisim);
		
		// özetle + operatörü hem toplama hemde tek bir satırda birleştirme amaçlı kullanılıyor.
		// - operatörü ilede çıkarma işlemlerimi yapıyorum
		// parantez içindeki işlemler en yüksek önceliğe sahiptir
		
		
		
	}
}
