
public class WorkSpace_Aritmetik_Operator {
	public static void main(String[] args) {

		// Aritmetik Atama  operatörlerü

		// += ilk önce topla , sonra çıkan sonucu değişkenime ata

		int x = 15;
		x += 5; // x değişkenimi 5 ile arttır çıkan sonucuda değişkenime ata
		System.out.println(x);

		// -= ilk önce çıkar , sonra çıkan sonucu değişkenime ata

		int y = 17;
		y -= 7; // y değişkeniminden 7'yi çıkart ve çıkan sonucu y değişkenime ata
		System.out.println(y);

		// *= ilk önce çarp , sonra çıkan sonucu değişkenime ata

		int z = 25;
		z *= 4;
		System.out.println(z);

		// /= ilk önce bölme işlemini yap , sonra çıkan sonucu değişkenime ata

		int q = 400;
		q /= 2;
		System.out.println(q);

		// %= ilk önce modunu al , sonra çıkan sonucu değişkenime ata

		int a = 134;
		a %= 10;
		System.out.println(a);

	}
}
